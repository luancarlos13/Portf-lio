// Quantidade inicial de projetos a exibir
const PROJETOS_INICIAIS = 6;
let projetosExibidos = PROJETOS_INICIAIS;

// Função para renderizar os cards de projetos
function renderizarProjetos() {
    const projetoCaixa = document.querySelector('.projetos-caixa');
    projetoCaixa.innerHTML = ''; // Limpa os cards antigos

    // Renderiza apenas os projetos até o limite
    for (let i = 0; i < projetosExibidos && i < projetos.length; i++) {
        const projeto = projetos[i];
        const card = document.createElement('div');
        card.className = 'projetos-card';
        card.innerHTML = `
            <img src="${projeto.imagem}" alt="${projeto.titulo}" class="projetos-imagem">
            <div class="caixa-textos-projeto">
                <h3 class="info-projetos">${projeto.titulo}</h3>
                <p class="paragrafo-projetos">${projeto.descricao}</p>
            </div>
    
            <a class="botao-projeto" href="${projeto.link}" target="_blank" rel="noopener noreferrer">Ver Projeto</a>
        `;
        projetoCaixa.appendChild(card);
    }

    // Se ainda há mais projetos, exibe o botão "TODOS OS PROJETOS"
    if (projetosExibidos < projetos.length) {
        const containerBotao = document.createElement('div');
        containerBotao.style.display = 'flex';
        containerBotao.style.justifyContent = 'center';
        containerBotao.style.padding = '2rem';
        const btnTodos = document.createElement('button');
        btnTodos.className = 'botao-todos-projetos';
        btnTodos.onclick = carregarMaisProjetos;
        btnTodos.textContent = 'TODOS OS PROJETOS'
        containerBotao.appendChild(btnTodos);
        projetoCaixa.parentElement.appendChild(containerBotao);
    }
}

// Função para carregar mais projetos
function carregarMaisProjetos() {
    projetosExibidos = projetos.length; // Carrega todos os projetos
    renderizarProjetos();
    
    // Scroll suave até a seção de projetos
    document.querySelector('#projetos').scrollIntoView({ behavior: 'smooth' });
}

// Renderizar projetos ao carregar a página
document.addEventListener('DOMContentLoaded', renderizarProjetos);

